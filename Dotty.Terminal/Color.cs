namespace Dotty.Terminal;

public enum ColorKind : byte
{
    Default,
    Ansi,
    AnsiBright,
    Indexed,
    Rgb,
}

public readonly struct Color : IEquatable<Color>
{
    public readonly ColorKind Kind;
    public readonly byte R;
    public readonly byte G;
    public readonly byte B;

    private Color(ColorKind kind, byte r, byte g, byte b)
    {
        Kind = kind;
        R = r;
        G = g;
        B = b;
    }

    public static Color DefaultColor => new(ColorKind.Default, 0, 0, 0);
    public static Color Ansi(byte index) => new(ColorKind.Ansi, index, 0, 0);
    public static Color AnsiBright(byte index) => new(ColorKind.AnsiBright, index, 0, 0);
    public static Color Indexed(byte index) => new(ColorKind.Indexed, index, 0, 0);
    public static Color FromRgb(byte r, byte g, byte b) => new(ColorKind.Rgb, r, g, b);

    public bool IsDefault => Kind == ColorKind.Default;

    public (byte R, byte G, byte B) ToRgb(Palette palette)
    {
        return Kind switch
        {
            ColorKind.Default => palette.Foreground,
            ColorKind.Ansi => palette.Colors[R],
            ColorKind.AnsiBright => palette.Colors[R + 8],
            ColorKind.Indexed => palette.Colors[R],
            ColorKind.Rgb => (R, G, B),
            _ => palette.Foreground,
        };
    }

    public (byte R, byte G, byte B) ToBgRgb(Palette palette)
    {
        return Kind switch
        {
            ColorKind.Default => palette.Background,
            _ => ToRgb(palette),
        };
    }

    public bool Equals(Color other) =>
        Kind == other.Kind && R == other.R && G == other.G && B == other.B;

    public override bool Equals(object? obj) => obj is Color c && Equals(c);
    public override int GetHashCode() => HashCode.Combine(Kind, R, G, B);
    public static bool operator ==(Color left, Color right) => left.Equals(right);
    public static bool operator !=(Color left, Color right) => !left.Equals(right);

    public override string ToString() => Kind switch
    {
        ColorKind.Default => "Default",
        ColorKind.Ansi => $"Ansi({R})",
        ColorKind.AnsiBright => $"AnsiBright({R})",
        ColorKind.Indexed => $"Indexed({R})",
        ColorKind.Rgb => $"Rgb({R},{G},{B})",
        _ => "Unknown",
    };
}

