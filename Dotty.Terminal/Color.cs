    private static void FillExtendedColors(Palette palette)
    {
        // 216 color cube (indices 16-231)
        for (int r = 0; r < 6; r++)
        for (int g = 0; g < 6; g++)
        for (int b = 0; b < 6; b++)
        {
            int idx = 16 + r * 36 + g * 6 + b;
            palette.Colors[idx] = (
                (byte)(r > 0 ? r * 40 + 55 : 0),
                (byte)(g > 0 ? g * 40 + 55 : 0),
                (byte)(b > 0 ? b * 40 + 55 : 0)
            );
        }

        // Grayscale ramp (indices 232-255)
        for (int i = 0; i < 24; i++)
        {
            byte v = (byte)(i * 10 + 8);
            palette.Colors[232 + i] = (v, v, v);
        }
    }
